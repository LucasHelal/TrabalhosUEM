    private void preencherFiltros() {
        preencherFiltroEspecie();
        preencherFiltroPorte();

    }

    private void preencherFiltroEspecie() {
        filtroEspecie.removeAllItems();
        filtroEspecie.addItem("Todos");
        for (Especie e : Especie.values()) {
            filtroEspecie.addItem(e);
        }
    }

    private void preencherFiltroPorte() {

        filtroPorte.removeAllItems();
        filtroPorte.addItem("Todos");
        for (Porte e : Porte.values()) {
            filtroPorte.addItem(e);
        }

    }

