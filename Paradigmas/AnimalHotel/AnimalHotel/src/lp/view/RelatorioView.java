    private boolean checarNoFiltro(Animal animal) {
        if (filtroEspecie.getSelectedItem() != null
                && filtroEspecie.getSelectedItem() != "Todos") {
            String e = (String) filtroEspecie.getSelectedItem().toString();
            if (e != null && !e.equals(animal.getEspecie().toString())) {
                return false;
            }
        }
        if (filtroPorte.getSelectedItem() != null
                && filtroPorte.getSelectedItem() != "Todos") {
            String e = (String) filtroPorte.getSelectedItem().toString();
            if (e != null && !e.equals(animal.getPorte().toString())) {
                return false;
            }
        }
        if (formaRelatorio.getSelectedIndex() == 0
                && BancoDeDados.getInstance().getEstadia(animal) == null) {
            return false;
        }
        return true;
    }

    private void preencheRelatorio() {
        relatorio.setText("");
        Set<Animal> ani = BancoDeDados.getInstance().getAnimais();
        Iterator<Animal> animais = ani.iterator();
        while (animais.hasNext()) {

            Animal animal = animais.next();
            if (checarNoFiltro(animal)) {
                relatorio.setText(relatorio.getText() + "\n"
                        + "Nome: " + animal.getNome() + "\n"
                        + "Altura: " + animal.getAltura() + "\n"
                        + "Comprimento: " + animal.getComprimento() + "\n"
                        + "Responsável: " + animal.getDono().getNome() + " Tel: " + animal.getDono().getTele() + "\n"
                        + "Segundo Resp.:" + animal.getResponsavel2().getNome() + " Tel: " + animal.getResponsavel2().getTele() + "\n"
                        + "Especie: " + animal.getEspecie() + "\n"
                        + "Porte: " + animal.getPorte() + "\n"
                        + "\n");
            }
        }
    }

